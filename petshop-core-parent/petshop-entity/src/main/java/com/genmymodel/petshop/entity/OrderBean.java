	public Element toXML(){
		Element xmlElement = new Element("Order");
		if (uuid != null)
			xmlElement.setAttribute(new Attribute("uuid", uuid));
		if (getDate() != null)
			xmlElement.setAttribute(new Attribute("date", getDate()));
		if (getReference() != null)
			xmlElement.setAttribute(new Attribute("reference", getReference()));
		if (getStatus() != null)
			xmlElement.setAttribute(new Attribute("status", getStatus()));
		xmlElement.setAttribute(new Attribute("id", String.valueOf(getId())));
		if (getCreationDate() != null)
			xmlElement.setAttribute(new Attribute("creationDate", getCreationDate().toString()));
		if (getUpdateDate() != null)
			xmlElement.setAttribute(new Attribute("updateDate", getUpdateDate().toString()));
		xmlElement.setAttribute(new Attribute("version", String.valueOf(getVersion())));
		
		if (!getItems().isEmpty()) {
			Element items = new Element("items");
			for (ItemBean tmp : getItems())
				items.addContent(new Element("Item", String.valueOf(tmp.getId())));
			xmlElement.addContent(items);
		}
		if (getPaymentAddress() != null)
			xmlElement.setAttribute("paymentAddress", String.valueOf(getPaymentAddress().getId()));
		if (getDeliveryAddress() != null)
			xmlElement.setAttribute("deliveryAddress", String.valueOf(getDeliveryAddress().getId()));
		if (getCart() != null)
			xmlElement.setAttribute("cart", String.valueOf(getCart().getId()));
		if (getCustomer() != null)
			xmlElement.setAttribute("customer", String.valueOf(getCustomer().getId()));
		
		return xmlElement;
	}

